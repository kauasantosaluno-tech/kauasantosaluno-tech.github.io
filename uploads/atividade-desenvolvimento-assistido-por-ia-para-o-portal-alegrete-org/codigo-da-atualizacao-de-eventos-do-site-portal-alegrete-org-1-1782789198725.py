import time
import os
from selenium import webdriver
from selenium.webdriver.chrome.service import Service
from selenium.webdriver.chrome.options import Options
from selenium.webdriver.common.by import By
from webdriver_manager.chrome import ChromeDriverManager

def extrair_eventos_glide(url):
    """
    Acessa a página do Glide usando um navegador invisível e extrai os textos visíveis.
    """
    print("Iniciando o navegador...")

    # Configurar o Chrome para rodar em modo headless (sem abrir a janela gráfica)
    chrome_options = Options()
    chrome_options.add_argument("--headless")
    chrome_options.add_argument("--no-sandbox") # Adicionado para rodar em ambientes como Colab
    chrome_options.add_argument("--disable-dev-shm-usage") # Adicionado para evitar problemas de memória em containers
    chrome_options.add_argument("--log-level=3")

    # Instala e inicializa o ChromeDriver automaticamente
    servico = Service(ChromeDriverManager().install())
    driver = webdriver.Chrome(service=servico, options=chrome_options)

    eventos_encontrados = []

    try:
        print(f"Acessando a página: {url}")
        driver.get(url)

        # O Glide demora alguns segundos para renderizar o banco de dados na tela.
        # Pausamos o script para garantir que os itens da programação apareçam.
        print("Aguardando o carregamento dos eventos dinâmicos...")
        time.sleep(10)

        # Estratégia: O Glide costuma organizar os itens de lista em divs com role="listitem"
        elementos = driver.find_elements(By.CSS_SELECTOR, "div[role='listitem']")

        if not elementos:
            print("Aviso: Nenhuma lista padrão encontrada. Tentando extrair blocos de texto principais...")
            # Fallback: pega o texto do corpo principal do app se a estrutura for diferente
            app_body = driver.find_element(By.ID, "app-root") or driver.find_element(By.TAG_NAME, "body")
            linhas = app_body.text.split('\n')
            # Filtramos linhas que tenham tamanho considerável para ser um evento
            eventos_encontrados = [linha.strip() for linha in linhas if len(linha.strip()) > 5]
        else:
            for elemento in elementos:
                # Pega todo o texto de dentro do card do evento e troca quebras de linha por hífen
                texto = elemento.text.strip()
                if texto:
                    eventos_encontrados.append(texto.replace('\n', ' | '))

    except Exception as e:
        print(f"Erro durante a raspagem de dados: {e}")
    finally:
        driver.quit()

    return eventos_encontrados

