def gerar_relatorio(eventos):
    """
    Compara os eventos encontrados com o portal principal e gera um relatório TXT.
    """
    nome_arquivo = "Relatorio_Atualizacao_Alegrete.txt"
    novos_eventos = []

    # Lógica de comparação (Filtro)
    for evento in eventos:
        if not evento_ja_cadastrado_no_portal(evento):
            novos_eventos.append(evento)

    # Criação do Arquivo TXT
    with open(nome_arquivo, "w", encoding="utf-8") as arquivo:
        arquivo.write("========================================================\n")
        arquivo.write(" RELATÓRIO DE ATUALIZAÇÃO - PROGRAMAÇÃO ALEGRETE.ORG \n")
        arquivo.write("========================================================\n")
        arquivo.write("Fonte verificada: https://eventos-alegrete.glide.page/dl/programacao\n\n")

        if not novos_eventos:
            arquivo.write("Nenhuma nova atualização encontrada. O portal parece estar em dia.\n")
        else:
            arquivo.write(f"Foram identificados {len(novos_eventos)} evento(s) ou informações que PODEM ser cadastrados no portal:\n\n")
            for i, evento in enumerate(novos_eventos, 1):
                arquivo.write(f"[{i}] {evento}\n")
                arquivo.write("-" * 60 + "\n")

    print(f"\n✅ Tarefa concluída! O arquivo '{nome_arquivo}' foi gerado na mesma pasta deste script.")

